ad = "arda"



print("merhaba ,  {:<10} ,  hoşgeldin".format(ad)) # sola doğru 10  karakterlik mesafeden sonra yazar

print("merhaba ,  {:>10} ,  hoşgeldin".format(ad)) # sağa doğru 10 karakterlik mesafeden sonra yazar

print("merhaba ,  {:^10} ,  hoşgeldin".format(ad)) # diğer metinleri 10 karakterlik bir boşluk açacak ve ortalayacak şekilde format fonk.'unun içideki değeri yazar

print("hoşgeldin,  {:s}".format("hasan")) # sadece karakter dizilerini almasını sağlar

print("hoşgeldin,  {:d}".format(31)) # sadece sayları yazar

print("hoşgeldin,  {:c}".format(255)) # 0-256 sayılarının ascii kodundaki karşılıklarını yazar






